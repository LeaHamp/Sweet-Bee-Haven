function changeLanguage(lang) {
    // Update active button state
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.textContent.toLowerCase() === lang.toUpperCase()) {
            btn.classList.add('active');
        }
    });

    // Update text content based on data attributes
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Store language preference
    localStorage.setItem('preferred-language', lang);
}

// Update HTML elements to include data-translate attributes
document.addEventListener('DOMContentLoaded', () => {
    // Set initial language based on stored preference or default to English
    const storedLang = localStorage.getItem('preferred-language') || 'en';
    changeLanguage(storedLang);
}); 